'''2.Write a Python program to convert a list to a tuple. 
Input: listx = [5, 10, 7, 4, 15, 3] 
Output: (5, 10, 7, 4, 15, 3)'''

# Define the list
list1 = [5, 10, 7, 4, 15, 3]

# Convert the list to a tuple
tuple1 = tuple(list1)

# Output the result
print("The Converted Tuple is: ", tuple1)

'''Output:
The Converted Tuple is: (5, 10, 7, 4, 15, 3)'''

