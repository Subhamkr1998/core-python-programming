# 2.Declare a square() function with one parameter.Then call the
# function and pass one number and display the square of that number

# Defining the function to calculate the square
def square(number):
    # Returning the square of the number
    return number ** 2

# Calling the function and passing a number
result = square(5)

# Displaying the result
print("The square of the number is:", result)


''' Output:
The square of the number is: 25
'''
