# 1. Declare a def() function with two parameters. Then call the function and pass two numbers and display their division. 

# Defining the function
def divide_numbers(num1, num2):
    # Performing division and returning the result
    return num1 / num2

# Calling the function and passing two numbers
result = divide_numbers(10, 2)

# Displaying the result
print("The result of the division is:", result)


'''Output :
The result of the division is: 5.0
'''
