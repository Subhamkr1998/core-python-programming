'''1. Write a Python program to find the number of times 4 appears in the tuple. 
Input: tuplex = (2, 4, 5, 6, 2, 3, 4, 4, 7 )
Output: 3 '''

# Define the tuple
tuple1 = (2, 4, 5, 6, 2, 3, 4, 4, 7)

# Count the occurrences of 4 in the tuple
count_of_4 = tuple1.count(4)

# Output the result
print("The number 4 appears", count_of_4, "times in the tuple.")

'''Output:
The number 4 appears 3 times in the tuple.'''
